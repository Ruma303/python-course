

#, Operatori di confronto

## Operatori unari
a = 35
print(+a)    # 35
print(-a)    # -35
print(not a) # False

## Valutazione intervalli
print(1 < 2 < 3)
print(10 >= 3 != 5)

## Valutazione logica
print(True and False)
print(True or False)
print(not True)

## Operatori logici
print(1 < 2 and 2 > 3)
print(1 < 2 or 2 > 3)
print(not 1 < 2 and 2 > 3)
print(not (3 < 2 or 2 > 3))